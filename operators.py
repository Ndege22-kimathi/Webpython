#Arithmetic Operators
from time import process_time_ns

x = 10
y = 4
#Addition
print(x + y)
#Subtraction
print(x - y)
#Multiplication
print(x * y)
#Division
print(x / y)
#Modulus: Returns the remainder
print(x % y)

#Comparison Operators
print(x < y)
print(x > y)
print(x <= y)
print(x >= y)
print(x == y) #Equal to
print(x != y) #Not Equal to

#Assignment Operators
w = 7
print(w)

#Operator Precedence
w += 2
print(w)

w -=4
print(w)

y /= 4
print(y)

#Logic Operators - And, Not, Or
print(34 < 4 and 56 > 21)
print(34 < 4 or 56 > 21)
print(not (34 < 4 and 56 > 21))




